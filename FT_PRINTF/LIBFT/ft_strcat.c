#include <stdio.h>

char	*ft_strcat(char *dest, const char *app)
{
	char *s;

	s = dest;
	while (*s != '\0')
		s++;
	while (*app != '\0')
		*s++ = *app++;
	*s = '\0';
	return (dest);
}
