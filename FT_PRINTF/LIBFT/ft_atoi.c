int	ft_atoi(const char *str)
{
	int	a;
	int	i;
	int f;

	i = 0;
	a = 0;
	f = 1;
	while (str[i] == '\f' || str[i] == '\v' || str[i] == '\n'
			|| str[i] == '\t' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-')
		f = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		a = a * 10 + (str[i] - '0');
		i++;
	}
	return (a * f);
}
